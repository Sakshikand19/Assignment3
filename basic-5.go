package main
import "fmt"
func main(){
	var x int16 = 170
	var y int16 =83
	fmt.Printf("addition : %d + %d = %d\n", x, y, x+y)
	fmt.Printf("subtraction: %d - %d = %d\n", x, y, x-y)
	fmt.Printf("multiplication : %d*%d = %d\n", x, y, x*y)
	fmt.Printf("division : %d / %d = %d\n", x, y, x/y)
	fmt.Printf("remainder : %d % %d = %d\n", x, y, x%y)



}